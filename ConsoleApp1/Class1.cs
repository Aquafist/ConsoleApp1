using System;
using System.Collections.Generic;
using System.Text;

namespace ConsoleApp1
{
    class Class1
    {
        public void Shape()
        {
            Console.WriteLine("Area of given shape is:");
        }
        public int Shape(int a)
        {
            return a * a;
        }
        public double Shape(double a)
        {
            return a * a;
        }
        public int Shape(int a, int b)
        {
            return a * b;
        }
    }
}